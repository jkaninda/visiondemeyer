We identify human resources, with particular emphasis on female youth who are our priority target, and design appropriate programs with the aim of forging their character and personality on the basis of ethical principles and values.
“VISION FRANÇOISE DEMEYER” is a set of rigorous programs that identify and prepare talented young people, before connecting them to a national and international professional market.
This upgrading process involves Educational Advice, Career Advice, scholarship opportunities and academic and professional internships.
Our objective is to prepare young people to face the job market, but also and above all, to help them assume themselves as future African leaders.
The creation of a citizen elite is the goal of all programs and actions designed, developed and executed by VFD.

